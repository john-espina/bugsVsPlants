package bug;

public class Speed {
	
	private float dx;
	private float dy;
	
	public Speed(float dx, float dy){
		this.dx= dx;
		this.dy = dy;
	}
	
	
	// Getter
	public float getDx() {return dx;}
	public float getDy() {return dy;}
	
	// Setter
	public void setDy(float dy) {this.dy = dy;}	
	public void setDx(float dx) {this.dx = dx;}


}
